bool
PageNumber_t::
Parse(
    const std::string& str)
{
    int page;
    int lastPage;
    if (2 == sscanf_s(str.c_str(), "Page %d of %d", &page, &lastPage)) {
        m_Data.Page = page;
        m_Data.LastPage = lastPage;
        return true;
    }
    return false;
}

