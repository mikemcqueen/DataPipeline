Character_t&
Account_t::
GetCharacter(
    ServerId_t     serverId,
    const wchar_t* pCharacterName)
{
    if (!m_spCharacter)
    {
        m_spCharacter.reset(new Character_t(*this, serverId, 0, pCharacterName));
    }
    return *m_spCharacter.get();
}

